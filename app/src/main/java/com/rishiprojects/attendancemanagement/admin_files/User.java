package com.rishiprojects.attendancemanagement.admin_files;

public class User {

    public String Name, Email, Username, Image, UID;

    public User() {

    }

    public User(String Name, String Email, String Username, String Image, String UID) {
        this.Name = Name;
        this.Email = Email;
        this.Username = Username;
        this.Image = Image;
        this.UID = UID;
    }
}
